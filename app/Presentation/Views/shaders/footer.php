        $(document).on('click', 'a[href*="product/addToCart"]', function(e) {
            e.preventDefault();
            let url = $(this).attr('href');
            let btn = $(this);
            let originalHtml = btn.html();
            let isBgPrimary = btn.hasClass('btn-primary');
            let isBgGradient = btn.hasClass('btn-gradient');
            
            btn.css('pointer-events', 'none').html('<i class="fas fa-spinner fa-spin mr-2"></i> Đang xử lý...');
            
            $.ajax({
                url: url,
                type: 'GET',
                dataType: 'json',
                success: function(res) {
                    if (res.success) {
                        // Cập nhật số lượng giỏ hàng trên Thanh Menu
                        let badge = $('#cart-badge');
                        badge.text(res.total_items);
                        if(badge.is(':hidden')) badge.show();
                        
                        // Hiệu ứng hoàn tất
                        btn.html('<i class="fas fa-check mr-2"></i> Đã thêm');
                        btn.removeClass('btn-primary btn-gradient btn-outline-gradient').addClass('btn-success text-white');
                        
                        setTimeout(() => {
                            btn.html(originalHtml);
                            btn.css('pointer-events', 'auto');
                            btn.removeClass('btn-success text-white');
                            if(isBgPrimary) btn.addClass('btn-primary');
                            if(isBgGradient) btn.addClass('btn-gradient');
                        }, 2000);
                    }
                },
                error: function() {
                    btn.html(originalHtml).css('pointer-events', 'auto');
                    alert('Đã xảy ra lỗi, vui lòng thử lại!');
                }
            });
        });
    });

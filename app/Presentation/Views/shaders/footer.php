    <script>
    function toggleChat() {
        var panel = document.getElementById('chat-panel');
        var fab = document.getElementById('chat-fab');
        if (panel.style.display === 'flex') {
            panel.style.display = 'none';
            fab.innerHTML = '<i class="fas fa-comments"></i><span class="fab-badge">AI</span>';
        } else {
            panel.style.display = 'flex';
            fab.innerHTML = '<i class="fas fa-times"></i>';
            document.getElementById('chat-input').focus();
            scrollChat();
        }
    }

    function scrollChat() {
        var box = document.getElementById('chat-messages');
        box.scrollTop = box.scrollHeight;
    }

    function mdLinksToHtml(text) {
        // Convert markdown [text](url) to HTML links
        text = text.replace(/\[([^\]]+)\]\(([^)]+)\)/g, '<a href="$2" target="_blank">$1</a>');
        // Convert **bold**
        text = text.replace(/\*\*([^*]+)\*\*/g, '<strong>$1</strong>');
        // Convert *italic*
        text = text.replace(/\*([^*]+)\*/g, '<em>$1</em>');
        // Convert newlines
        text = text.replace(/\n/g, '<br>');
        return text;
    }

    function sendChat() {
        var input = document.getElementById('chat-input');
        var msg = input.value.trim();
        if (!msg) return;

        var box = document.getElementById('chat-messages');

        // Add user message
        var userDiv = document.createElement('div');
        userDiv.className = 'chat-msg user';
        userDiv.textContent = msg;
        box.appendChild(userDiv);
        input.value = '';
        scrollChat();

        // Show typing indicator
        var typing = document.createElement('div');
        typing.className = 'chat-typing';
        typing.id = 'typing-indicator';
        typing.innerHTML = '<span></span><span></span><span></span>';
        box.appendChild(typing);
        scrollChat();

        // Disable input
        input.disabled = true;
        document.getElementById('chat-send').disabled = true;

        fetch('/webbanhang/index.php?url=chat/send', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ message: msg })
        })
        .then(function(r) { return r.json(); })
        .then(function(data) {
            var t = document.getElementById('typing-indicator');
            if (t) t.remove();

            var botDiv = document.createElement('div');
            botDiv.className = 'chat-msg bot';
            botDiv.innerHTML = mdLinksToHtml(data.reply || 'Xin lỗi, đã có lỗi xảy ra.');
            box.appendChild(botDiv);
            scrollChat();
        })
        .catch(function() {
            var t = document.getElementById('typing-indicator');
            if (t) t.remove();
            var errDiv = document.createElement('div');
            errDiv.className = 'chat-msg bot';
            errDiv.textContent = 'Lỗi kết nối. Vui lòng thử lại! 🙏';
            box.appendChild(errDiv);
            scrollChat();
        })
        .finally(function() {
            input.disabled = false;
            document.getElementById('chat-send').disabled = false;
            input.focus();
        });
    }

    document.getElementById('chat-input').addEventListener('keydown', function(e) {
        if (e.key === 'Enter') sendChat();
    });
    </script>

